package Response;

public class Result {
    Anilist anilist;
    String filename;
    String episode;
    Double from;
    Double to;
    Double similarity;
    String video;
    String image;

    public Anilist getAnilist() {
        return anilist;
    }

    public String getFilename() {
        return filename;
    }

    public String getEpisode() {
        return episode;
    }

    public Double getFrom() {
        return from;
    }

    public Double getTo() {
        return to;
    }

    public Double getSimilarity() {
        return similarity;
    }

    public String getVideo() {
        return video;
    }

    public String getImage() {
        return image;
    }
}
